package Day4;

//entity class

class Person {

    private String name;
    private int rollno;
    private int age;

    //getter methods to access the private variables

    public String getName(){
        return name;
    }
    public int getRollno(){
        return rollno;
    }
    public int getAge(){
        return age;
    }

    // setter methods

    public void setName(String name) {
        this.name = name;
    }

    public void setRollno(int rollno) {
        this.rollno = rollno;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public static void main(String[] aname) {
        aname= aname;
    }
}
